    public void cargarTablas(){
        try {
            String tablaSeleccionada = (String)cmbTablas.getSelectedItem();
            Connection conexionTabla = Conexion.mySQL("proyecto_final", "root", "");
            Statement sentenciaTabla = conexionTabla.createStatement();
            ResultSet resultadoTabla = sentenciaTabla.executeQuery("show tables;");
            cmbTablas.removeAllItems();
            while(resultadoTabla.next()){
                cmbTablas.addItem(resultadoTabla.getString(1));
            }
            cmbTablas.setSelectedItem(1);
        } catch (SQLException ex) {
            Logger.getLogger(ModificarDatos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public void consultarDatos(Object itemSeleccionado){
        try {
            String tablaSeleccionada = (String)itemSeleccionado;
            Connection conexionTabla = Conexion.mySQL("proyecto_final", "root", "");
            Statement sentenciaTabla = conexionTabla.createStatement();
            String sqlTabla = "SELECT * FROM "+tablaSeleccionada+";";
            ResultSet resultadoTabla = sentenciaTabla.executeQuery(sqlTabla);
            //Obtener el metadato del resultset para saber el nombre de las columnas de la tabla seleccionada
            ResultSetMetaData rsmd = resultadoTabla.getMetaData();
            //Borrar datos cada vez que se consulta
            modeloTabla.setRowCount(0);
            modeloTabla.setColumnCount(0);
            int contadorColumnas = rsmd.getColumnCount();
            //AÃ±adir las columnas al modelo de la tabla
            for (int i = 0; i < contadorColumnas; i++) {
                modeloTabla.addColumn(rsmd.getColumnLabel(i+1));
            }
            while(resultadoTabla.next()){
                String fila[] = new String[contadorColumnas];
                for (int j = 0; j < contadorColumnas ; j++) {
                     fila[j]=resultadoTabla.getString(j+1);
                }
                modeloTabla.addRow(fila);
            }
            table.setModel(modeloTabla);
        } catch (SQLException ex) {
            Logger.getLogger(ModificarDatos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
